#include <stdio.h>

char		*ft_rot42(char *str)
{
	char	*ptr;

	ptr = str;
	while (*ptr)
	{
		if (*ptr >= 65 && *ptr <= 90)
			*ptr = (*ptr - 65 + 42) % 26 + 65;
		else if (*ptr >= 97 && *ptr <= 122)
			*ptr = (*ptr - 97 + 42) % 26 + 97;
		ptr++;
	}
	return (str);
}
